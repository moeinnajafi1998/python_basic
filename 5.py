# Data Structures:

# 1.	Lists:
fruits = ["apple", "banana", "cherry"]
print(fruits[0])  # Output: apple

# 2.	Tuples:
coordinates = (10, 20)
print(coordinates[1])  # Output: 20

# 3.	Dictionaries:
person = {"name": "Alice", "age": 25}
print(person["name"])  # Output: Alice

# 4.	Sets:
unique_numbers = {1, 2, 3, 4, 4, 5}
print(unique_numbers)  # Output: {1, 2, 3, 4, 5}